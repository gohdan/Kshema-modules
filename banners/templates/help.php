<li>Название для вывода пользователю может содержать любые символы. Используется для показа посетителям сайта, составления меню.</li>
</ul>

<h3><a name="categories_del">Удаление</a></h3>

<p>При удалении категории удаляются также и все принадлежащие к ней элементы.</p>

<h2><a name="banners">Баннеры</a></h2>

<h3><a name="banners_common">Общие сведения</a></h3>


<h3><a name="banners_add">Добавление</a></h3>

<ul>
<li>Системное название должно состоять из букв латинского алфавита и цифр, может содержать знак подчёркивания.</li>
<li>Название для вывода пользователю может содержать любые символы. Оно будет показано пользователю в свойстве "title" баннера-изображения, 
списке баннеров и т. п.</li>
<li>Категория - каждая новость относится к какой-либо категории.</li>
<li>Файл - собственно файл баннера.</li>
<li>Тип - тип баннера. Пока поддерживаются только простые изображения.</li>
<li>Описание - более подробное описание баннера.</li>
<li>Alt - в случае, если тип баннера - изображение, содержимое этого поля будет показано в свойстве "alt" этого изображения.</li>
<li>Ширина - ширина изображения или flash-ролика.</li>
<li>Высота - высота изображения или flash-ролика.</li>
</ul>

