<h2><a name="db">База данных</a></h2>

<h3><a name="db_common">Общие сведения</a></h3>

<p>В базе данных новости хранятся в следующих таблицах:</p>

<ul>
<li>ksh_banners_categories - категории новостей;</li>
<li>ksh_banners - сами новости.</li>
</ul>

<p>На диске элементы, принадлежащие к новостям, хранятся в каталоге /uploads/banners.</p>

