<h2><a name="db">База данных</a></h2>

<h3><a name="db_common">Общие сведения</a></h3>

<p>В базе данных страницы хранятся в следующих таблицах:</p>

<ul>
<li>ksh_pages_categories - категории страниц;</li>
<li>ksh_pages - сами страницы.</li>
</ul>

<p>На диске элементы, принадлежащие к страницам, хранятся в каталоге /uploads/pages.</p>

