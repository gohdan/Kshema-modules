<h2><a name="pages">Страницы</a></h2>

<h3><a name="pages_common">Общие сведения</a></h3>

<h3><a name="pages_add">Добавление</a></h3>

<ul>
<li>Системное название - название, по которому страницу идентифицирует система. 
Должно состоять из латинских букв и цифр, может содержать символ подчёркивания.</li>
<li>Название для показа пользователю может содержать любые символы. Оно может быть показано пользователю в списке страниц, меню и т. п.</li>
<li>Категория - каждая страница относится к какой-либо категории.</li>
<li>Ключевые слова - будут в неизменном виде перенесены в meta keywords.</li>
<li>Описание - будет в неизменном виде перенесено в meta description.</li>
<li>Общий шаблон - шаблон всей страницы. Должен состоять из латинских букв и цифр, может содержать символ подчёркивания. По содержимому 
поля система будет пытаться отыскать PHP-скрипт, содержащий шаблон.</li>
<li>Шаблон меню - шаблон меню. Должен состоять из латинских букв и цифр, может содержать символ подчёркивания. По содержимому 
поля система будет пытаться отыскать PHP-скрипт, содержащий шаблон.</li>
<li>Содержание - собственно текст страницы. Может содержать любые символы, HTML-код, может заполняться с помощью визуального редактора.</li>
</ul>

